package edu.mit.sips.sim.util;

/**
 * The Interface Units.
 */
public interface Units {
	
	/**
	 * Gets the name.
	 *
	 * @return the name
	 */
	public String getName();
	
	/**
	 * Gets the abbreviation.
	 *
	 * @return the abbreviation
	 */
	public String getAbbreviation();
	
	/**
	 * Gets the scale.
	 *
	 * @return the scale
	 */
	public double getScale();
}
